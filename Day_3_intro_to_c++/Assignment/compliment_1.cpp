// If we have only 4 bits, How 3 and -6 will be written in 4 bits.

/* 

Representing 3:

The binary representation of 3 in 4 bits is 0011.
Representing -6:

To represent -6 using Two's complement, you first find the binary representation of 6, which is 0110.
Then, invert all the bits (change 0s to 1s and 1s to 0s), resulting in 1001.
Finally, add 1 to the inverted value, yielding 1010.
So, in a 4-bit signed integer representation:

3 is represented as 0011.
-6 is represented as 1010. 

*/