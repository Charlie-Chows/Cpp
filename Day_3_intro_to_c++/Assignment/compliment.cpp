// If we have only 5 bits, How 13 and -16 will be written in 5 bits.

/* 

To represent 13 in 5 bits: 
01101

To represent -16 in 5 bits using two's complement:
Binary representation of 16: 
10000

Invert the bits: 
01111

Add 1: 
10000

So, the correct representation of -16 in 5 bits using two's complement is 
10000. 

The binary representation of 16 and its negative counterpart, -16, are the 
same when using two's complement representation. This is a property of two's 
complement encoding.

*/